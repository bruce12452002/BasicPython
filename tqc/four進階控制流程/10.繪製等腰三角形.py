"""
請撰寫一程式，依照使用者輸入的n，畫出對應的等腰三角形

範例輸入
7
範例輸出

      *
     ***
    *****
   *******
  *********
 ***********
*************
"""
'''
行 1 2 3 4 5 6  7
空 6 5 4 3 2 1  0
星 1 3 5 7 9 11 13
空公式：最大行-行
星公式：行*2-1
'''
n = 7
for i in range(1, n + 1):
    for j in range(1, n - i + 1):
        print(" ", end="")
    for j in range(1, i * 2 - 1 + 1):
        print("*", end="")
    print()

# 利用字串的 *
for i in range(1, n + 1):
    print(" " * (n - i) + "*" * (i * 2 - 1))
