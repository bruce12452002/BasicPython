"""
請撰寫一程式，讓使用者輸入一個正整數，將此正整數以反轉的順序輸出，並判斷如輸入0，則輸出為0

範例輸入1
31283
範例輸出1
38213

範例輸入2
0
範例輸出2
0

範例輸入3
135790
範例輸出3
097531
"""
n = 31283


# 方法一： 除 10 取餘
TEN = 10

while n // TEN != 0:
    print(n % TEN, end="")
    n //= TEN
print(n)

# 方法二: slice
n = 31283
str_number = str(n)
print(str_number[::-1])
